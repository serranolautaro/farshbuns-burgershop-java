package modelos;

public class Pago {

	private float importe_total;
	private float costo_por_envio;
	
	public float getImporte_total() {
		return importe_total;
	}
	public void setImporte_total(float importe_total) {
		this.importe_total = importe_total;
	}
	public float getCosto_por_envio() {
		return costo_por_envio;
	}
	public void setCosto_por_envio(float costo_por_envio) {
		this.costo_por_envio = costo_por_envio;
	}
	
	
}
